# and（与）必须都为真，一假则假；若值都为真，值取后不取前
# print(True and True)    # True
# print(True and False)   # False
# print(False and True)   # False
# print(False and False)  # False
# print(20 and 30)    # 30
# print(40 and 30)    # 30
# print(40 and 0)     # 0
# print(0 and 30)     # 0

# or（或） 有真则真，全假则假;若值都为真，值取前不取后
# print(True or True) # True
# print(True or False)    # True
# print(False or True)    # True
# print(False or False)   # False
# print(10 or 20)     # 10
# print(20 or 10)     # 20
# print(0 or 10)      # 10
# print(30 or 0)      # 30

# not(非） 取反，真变假，假变真
# print(not True)     # False
# print(not False)    # True
# print(not 20)       # False
# print(not 0)        # True
# print(not -20)      # False




















