import math

# math的相关函数：
# math.ceil()		向上取整	与内置函数 round()类似
# 	round()方法是四舍五入
# math.ceil(2.25)	==>	3
# round(2.25)	==>	2
# round(2.55)	==>	3
#
# math.floor()	向下取整	与内置函数
# math.floor(2.55)	==>	2
#
# math.pow()		计算数值的n次方,结果是浮点数
# math.pow(2,3)	==>	8.0
#
# math.sqrt()		开平方运算，结果是浮点数
# math.sqrt(4)	==>	2.0
#
# math.fabs()		计算绝对值,结果是浮点数
# math.fabs(-100) ==> 100.0
# math.fabs(-3.14) ==> 3.14
#
# math.modf()		把一个数值拆分成整数和小数组成的元组，结果为浮点数
# math.modf(3)	==>	(0.0 , 3.0)
#
# math.copysign(x,y)	把参数y的正负符号拷贝给参数x,结果为浮点数
# math.copysign(3.15,-99)	==>	-3.15
# math.copysign(3.15,99)	==>	3.15
# math.copysign(-3.15,-99)	==>	-3.15
#
# math.fsum()		将一个容器类型数据中的元素进行求和运算，结果为浮点数
# math.fsum('123')	==>	报错，不能给字符串
# math.fsum((1,2,3))	==>	6	注意：容器中的元素必须是可以运算的number类型
#
# math.factorial(x)    返回x的阶乘,即：1*2*3*...*(x-1)x，返回值是一个整数
# math.factorial(3)     ==> 1*2*3   ==> 6

